// Look in the process table for an UNUSED proc.
// If found, change state to EMBRYO and initialize
// state required to run in the kernel.
// Otherwise return 0.
static struct proc* allocproc(void) {
    struct proc *p;
    char *sp;

    acquire(&ptable.lock);

    // UNUSED 상태의 프로세스 검색
    for (p = ptable.proc; p < &ptable.proc[NPROC]; p++) {
        if (p->state == UNUSED)
            goto found;
    }
    
    release(&ptable.lock);
    return 0;

found:
    p->state = EMBRYO;
    p->pid = nextpid++;

    // 프로세스 확인
    if (strcmp_xv6(p->name, "sh") == 0 || strcmp_xv6(p->name, "idle") == 0 || strcmp_xv6(p->name, "init") == 0) {
        p->q_level = 3;  // shell, idle, init 프로세스는 항상 레벨 3
    } else {
        p->q_level = 0;  // 이후 생성된 프로세스는 레벨 0에서 시작
    }

    p->cpu_burst = 0;
    p->cpu_usage = 0;
    p->cpu_wait = 0;
    p->io_wait_time = 0;
    p->end_time = -1;
    p->is_q_timeout = 0;
    p->aging_time = 0; 

    // 프로세스 생성 시 출력 추가
    if (p->pid >= 4) {
        #ifdef DEBUG
        cprintf("PID: %d created\n", p->pid);
        #endif
    }
    
    release(&ptable.lock);

    // 커널 스택 할당
    if ((p->kstack = kalloc()) == 0) {
        p->state = UNUSED;
        return 0;
    }
    sp = p->kstack + KSTACKSIZE;

    sp -= sizeof *p->tf;
    p->tf = (struct trapframe*)sp;

    sp -= 4;
    *(uint*)sp = (uint)trapret;

    sp -= sizeof *p->context;
    p->context = (struct context*)sp;
    memset(p->context, 0, sizeof *p->context);
    p->context->eip = (uint)forkret;

    return p;
}

